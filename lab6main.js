//Upon hovering on a navigaiton item, the background colour of the navigation item will turn light pink
function mouseOver(navItem) {
    navItem.style.backgroundColor = "lightpink";
}

//Once the mouse is removed from hovering over the navigaiotn item, the backgorund colour of the item will revert back
function mouseOut(navItem) {
    navItem.style.backgroundColor = "";
}